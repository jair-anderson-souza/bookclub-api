package io.github.jass2125.beer.api.beer.resource;

public class BeerFilter {

    private String name;
    private String food;

    public BeerFilter() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFood() {
        return food;
    }

    public void setFood(String food) {
        this.food = food;
    }

}
